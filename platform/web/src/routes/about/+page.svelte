  <span class="eyebrow">About</span>
  <h1 style="margin-top: 0.6rem;">The manual a senior who actually cares would hand you.</h1>
  <p class="tagline">A free, text-first library of the real-world developer knowledge nobody ever sat you down and explained.</p>

  <p>Every developer hits the same wall. You can use the tools — Git, the command line, a deploy pipeline — but the moment something goes sideways, your stomach drops, because no one ever told you what's <em>actually</em> happening underneath. The official docs assume you already know. The tutorials stop at “hello world.” The real knowledge lives in senior engineers' heads and gets passed down by luck.</p>

  <p>This is the manual that should have come with the job. Written like advice from a battle-hardened friend — calm, specific, zero ego.</p>

  <h2>What this is</h2>
  <ul>
    <li><strong>Text-first.</strong> No video walls, no 40-minute intros. You can read, search, and skim.</li>
    <li><strong>Real-world.</strong> Not “build a todo app.” The things that actually bite you in production and on teams.</li>
    <li><strong>Explained, not referenced.</strong> Mental models first, so the commands stop feeling like magic spells.</li>
    <li><strong>Free forever.</strong> No ads, no paywalls, no account required.</li>
  </ul>

  <h2>Who it's for</h2>
  <p>Junior and mid-level developers drowning in undocumented reality — and anyone who wants to genuinely <em>understand</em> the tools that run the world, instead of memorizing incantations and hoping.</p>

  <blockquote><p>The one thing to remember: this is the manual a senior who actually cares would hand you. Calm, trustworthy, with a face.</p></blockquote>

